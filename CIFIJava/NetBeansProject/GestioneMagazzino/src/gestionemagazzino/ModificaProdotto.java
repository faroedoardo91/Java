    private void btnInviaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInviaActionPerformed
        // TODO add your handling code here:
        // parametri per accesso al database
        
        try {
            // load del driver
            Class.forName(driver);

            // effettuo la connessione al database
            Connection connection = DriverManager.getConnection(url, userDB, passwordDB);

            // costruisco la sintassi sql per dialogare con il database
            String sql = "Update prodotti set nome=?,descrizione=?,prezzo=?,giacenza=? "
                    + " where codice=?";

            // istanzio l'oggetto st per dialogare con il database
            PreparedStatement ps = connection.prepareStatement(sql);

            ps.setString(1, txtNome.getText());
            ps.setString(2, txtDescrizione.getText());
            ps.setDouble(3, Double.parseDouble(txtPrezzo.getText()));
            ps.setInt(4, Integer.parseInt(txtGiacenza.getText()));
            ps.setInt(5, Integer.parseInt(txtCodice.getText()));
            
            // estraggo i dati dal database
            int rows = ps.executeUpdate();

            if (rows > 0) {
                JOptionPane.showMessageDialog(this, "Modifica dati avvenuta con successo");
            } else {
                JOptionPane.showMessageDialog(this, "Modifica fallita");
            }

            ps.close(); // rilascio la risorsa st
            connection.close(); // finalizzo la connessione al database
            resetForm();
        } catch (ClassNotFoundException e) {
            // TODO Auto-generated catch block
            JOptionPane.showMessageDialog(this, e.getMessage());
        } catch (SQLException e) {
            // TODO Auto-generated catch block
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnInviaActionPerformed

