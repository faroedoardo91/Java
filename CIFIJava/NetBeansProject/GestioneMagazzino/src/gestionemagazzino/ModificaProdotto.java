    private void btnCercaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCercaActionPerformed
        // TODO add your handling code here:
        // parametri per accesso al database

        try {
            // load del driver
            Class.forName(driver);

            // effettuo la connessione al database
            Connection connection = DriverManager.getConnection(url, userDB, passwordDB);

            // costruisco la sintassi sql per dialogare con il database
            String sql = "Select * from prodotti where codice=?";

            // istanzio l'oggetto st per dialogare con il database
            PreparedStatement ps = connection.prepareStatement(sql);

            ps.setInt(1, Integer.parseInt(txtCerca.getText()));
            

            // estraggo i dati dal database
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                txtCodice.setText(rs.getInt("Codice")+"");
                txtNome.setText(rs.getString("Nome"));
                txtDescrizione.setText(rs.getString("Descrizione"));
                txtPrezzo.setText(rs.getDouble("Prezzo")+"");
                txtGiacenza.setText(rs.getInt("Giacenza")+"");
                
            } else {
                JOptionPane.showMessageDialog(this, "Prodotto non trovato");
                resetForm();
            }
            rs.close();
            ps.close(); // rilascio la risorsa st
            connection.close(); // finalizzo la connessione al database
            
        } catch (ClassNotFoundException e) {
            // TODO Auto-generated catch block
            JOptionPane.showMessageDialog(this, e.getMessage());
        } catch (SQLException e) {
            // TODO Auto-generated catch block
            JOptionPane.showMessageDialog(this, e.getMessage());
        }


    }//GEN-LAST:event_btnCercaActionPerformed

